		<ol>
			<li>Préparer les ustensiles (couteau, cuillère, saladier, verre doseur ou balance, papier sulfurisé et plaque allant au four) et les ingrédients, sortir le beurre du frigo et préchauffer le four à 180°C</li> 
			<li>Mettre dans le saladier la farine + le sucre + le bicarbonate et mélanger</li> 
